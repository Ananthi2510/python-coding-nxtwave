#To find the season of the given month number

month = int(input())

if month == 11 or month == 12 or month == 1:
    print("Winter")
elif month == 2 or month == 3:
    print("Spring")
elif month == 4 or month == 5 or month == 6:
    print("Summer")
elif month == 7 or month == 8:
    print("Rainy")
elif month == 9 or month == 10:
    print("Autumn")
